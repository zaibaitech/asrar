/**
 * Get Divine Name by number (1-99)
 */
export function getDivineNameByNumber(number: number): DivineName | undefined {
  return DIVINE_NAMES.find(name => name.number === number);
}

/**
 * Calculate Divine Name resonance based on Hadad value
 * Hadad % 99, where 0 becomes 99
 */
export function calculateDivineNameResonance(hadad: number): DivineName | undefined {
  let remainder = hadad % 99;
  if (remainder === 0) remainder = 99;
  return getDivineNameByNumber(remainder);
}

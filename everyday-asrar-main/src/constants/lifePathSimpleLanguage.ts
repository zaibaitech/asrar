/**
 * Get simplified term based on user preference
 */
export function getSimpleTerm(
  key: keyof typeof SIMPLE_TERMS.en,
  language: 'en' | 'fr' | 'ar' = 'en',
  useJargon: boolean = false
): string {
  const terms = SIMPLE_TERMS[language][key];
  if (!terms) return '';
  
  if (typeof terms === 'object' && 'simple' in terms && 'jargon' in terms) {
    return useJargon ? terms.jargon : terms.simple;
  }
  
  return String(terms);
}

/**
 * Get tooltip for a term
 */
export function getTooltip(
  key: keyof typeof SIMPLE_TERMS.en,
  language: 'en' | 'fr' | 'ar' = 'en'
): string {
  const terms = SIMPLE_TERMS[language][key];
  if (typeof terms === 'object' && 'tooltip' in terms) {
    return terms.tooltip;
  }
  return '';
}
